package com.wn.loanapp.dto;

public class LoanDispersedDTO {
	
	private String txnId;
	
	private String onlinePaymentId;
	
	private String retailerName;
	
	private String amount;
	
	private String tnDate;
	
	private String repayTxnId;
	
	private String settleAmt;
	
	private String verify;

	/**
	 * @return the txnId
	 */
	public String getTxnId() {
		return txnId;
	}

	/**
	 * @param txnId the txnId to set
	 */
	public void setTxnId(String txnId) {
		this.txnId = txnId;
	}

	/**
	 * @return the onlinePaymentId
	 */
	public String getOnlinePaymentId() {
		return onlinePaymentId;
	}

	/**
	 * @param onlinePaymentId the onlinePaymentId to set
	 */
	public void setOnlinePaymentId(String onlinePaymentId) {
		this.onlinePaymentId = onlinePaymentId;
	}

	/**
	 * @return the retailerName
	 */
	public String getRetailerName() {
		return retailerName;
	}

	/**
	 * @param retailerName the retailerName to set
	 */
	public void setRetailerName(String retailerName) {
		this.retailerName = retailerName;
	}

	/**
	 * @return the amount
	 */
	public String getAmount() {
		return amount;
	}

	/**
	 * @param amount the amount to set
	 */
	public void setAmount(String amount) {
		this.amount = amount;
	}

	/**
	 * @return the tnDate
	 */
	public String getTnDate() {
		return tnDate;
	}

	/**
	 * @param tnDate the tnDate to set
	 */
	public void setTnDate(String tnDate) {
		this.tnDate = tnDate;
	}

	/**
	 * @return the repayTxnId
	 */
	public String getRepayTxnId() {
		return repayTxnId;
	}

	/**
	 * @param repayTxnId the repayTxnId to set
	 */
	public void setRepayTxnId(String repayTxnId) {
		this.repayTxnId = repayTxnId;
	}

	/**
	 * @return the settleAmt
	 */
	public String getSettleAmt() {
		return settleAmt;
	}

	/**
	 * @param settleAmt the settleAmt to set
	 */
	public void setSettleAmt(String settleAmt) {
		this.settleAmt = settleAmt;
	}

	/**
	 * @return the verify
	 */
	public String getVerify() {
		return verify;
	}

	/**
	 * @param verify the verify to set
	 */
	public void setVerify(String verify) {
		this.verify = verify;
	}
	
	
}
