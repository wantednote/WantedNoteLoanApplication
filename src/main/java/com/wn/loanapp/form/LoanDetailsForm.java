package com.wn.loanapp.form;

public class LoanDetailsForm {
	
	private String orderNo;
	
	private String distributorName;
	
	private String firstName;
	
	private String tnDateStart;
	
	private String tnDateEnd;
	
	private Long amount;
	
	private String distributer;
	
	private int start;
	
	private int length;

	/**
	 * @return the orderNo
	 */
	public String getOrderNo() {
		return orderNo;
	}

	/**
	 * @param orderNo the orderNo to set
	 */
	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}

	/**
	 * @return the distributorName
	 */
	public String getDistributorName() {
		return distributorName;
	}

	/**
	 * @param distributorName the distributorName to set
	 */
	public void setDistributorName(String distributorName) {
		this.distributorName = distributorName;
	}

	/**
	 * @return the firstName
	 */
	public String getFirstName() {
		return firstName;
	}

	/**
	 * @param firstName the firstName to set
	 */
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	
	/**
	 * @return the tnDateStart
	 */
	public String getTnDateStart() {
		return tnDateStart;
	}

	/**
	 * @param tnDateStart the tnDateStart to set
	 */
	public void setTnDateStart(String tnDateStart) {
		this.tnDateStart = tnDateStart;
	}

	/**
	 * @return the tnDateEnd
	 */
	public String getTnDateEnd() {
		return tnDateEnd;
	}

	/**
	 * @param tnDateEnd the tnDateEnd to set
	 */
	public void setTnDateEnd(String tnDateEnd) {
		this.tnDateEnd = tnDateEnd;
	}

	/**
	 * @return the amount
	 */
	public Long getAmount() {
		return amount;
	}

	/**
	 * @param amount the amount to set
	 */
	public void setAmount(Long amount) {
		this.amount = amount;
	}
	
	/**
	 * @return the distributer
	 */
	public String getDistributer() {
		return distributer;
	}

	/**
	 * @param distributer the distributer to set
	 */
	public void setDistributer(String distributer) {
		this.distributer = distributer;
	}

	/**
	 * @return the start
	 */
	public int getStart() {
		return start;
	}

	/**
	 * @param start the start to set
	 */
	public void setStart(int start) {
		this.start = start;
	}

	/**
	 * @return the length
	 */
	public int getLength() {
		return length;
	}

	/**
	 * @param length the length to set
	 */
	public void setLength(int length) {
		this.length = length;
	}
	
}	
